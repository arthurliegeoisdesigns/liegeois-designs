/**
 * gen-home-data.mjs — regenerates src/content/home-work.ts from
 * case-studies.ts. The homepage must never bundle the full case-study
 * text; this extracts only the light fields. Rerun after adding studies:
 *   node scripts/gen-home-data.mjs
 */
import { readFileSync, writeFileSync } from 'node:fs'

const src = readFileSync('src/content/case-studies.ts', 'utf8')
const CDN_CONST = (src.match(/const CDN = '([^']+)'/) || [])[1] || ''
const resolveImg = (u) => (u ? u.replace('${CDN}', CDN_CONST) : u)

const blocks = src.split(/\n  \{\n/).slice(1)
const entries = []
for (const b of blocks) {
  const g = (re) => (b.match(re) || [])[1]
  const slug = g(/slug: '([^']+)'/)
  if (!slug) continue
  entries.push({
    slug,
    client: (g(/client: '((?:[^'\\]|\\.)+)'/) || '').replace(/\\'/g, "'"),
    project: (g(/project: '((?:[^'\\]|\\.)+)'/) || '').replace(/\\'/g, "'"),
    format: g(/format: '([^']+)'/),
    year: Number(g(/year: (\d+)/)),
    featured: /featured: true/.test(b),
    order: Number(g(/order: (\d+)/) ?? 99),
    image: resolveImg(g(/images: \[\n\s+[`']([^`'\n]+)[`']/) || g(/images: \[\s*[`']([^`'\n]+)[`']/)),
  })
}
const lite = entries.map(({ featured: _f, order: _o, ...e }) => e)
const featured = entries
  .filter((e) => e.featured)
  .sort((a, b) => a.order - b.order)
  .slice(0, 6)
  .map(({ featured: _f, order: _o, ...e }) => e)

const out = `// ─────────────────────────────────────────────────────────────
// GENERATED by scripts/gen-home-data.mjs — do not edit by hand.
// Light work data for the homepage bundle (full text stays out of it).
// Rerun after editing case-studies.ts:  node scripts/gen-home-data.mjs
// ─────────────────────────────────────────────────────────────

export type WorkLite = {
  slug: string
  client: string
  project: string
  format: string
  year: number
  image: string
}

export const TOTAL_PROJECTS = ${entries.length}

export const featuredWork: WorkLite[] = ${JSON.stringify(featured, null, 2)}

export const workLiteBySlug: Record<string, WorkLite> = Object.fromEntries(
  (${JSON.stringify(lite, null, 2)} as WorkLite[]).map((w) => [w.slug, w]),
)
`
writeFileSync('src/content/home-work.ts', out)
console.log(`entries: ${entries.length}, featured: ${featured.length}`)
console.log(featured.map((f) => `${f.slug} | ${f.image?.slice(0, 75)}`).join('\n'))
